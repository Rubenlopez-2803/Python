e = 2.718281828       # Número de punto flotante que ocupa 10 espacios y tiene 3 decimales
print(f'{e:10.3f}')
print(f'{e:010.5f}')  # Cinco decimales y ceros a la izquierda.
print(f'{e:10.0f}')   # Igual que el primero, con cero decimales
print(f'{e:10.4e}')   # Igual que el primero, pero en notación científica
